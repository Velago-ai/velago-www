import { jsPDF } from "jspdf";
import type { Category, ChatSession } from "./placeholder-data";

const BRAND = { primary: "#3D5AFE", text: "#0A1130", muted: "#5A6480", soft: "#F4F6FF" };

function header(doc: jsPDF, title: string, subtitle: string) {
  doc.setFillColor(BRAND.primary);
  doc.rect(0, 0, 210, 28, "F");
  doc.setTextColor("#FFFFFF");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(18);
  doc.text("VelaGo", 14, 13);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  doc.text("velago.ai · Voice booking", 14, 20);

  doc.setTextColor(BRAND.text);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(20);
  doc.text(title, 14, 44);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(11);
  doc.setTextColor(BRAND.muted);
  doc.text(subtitle, 14, 51);
}

function detailsBlock(doc: jsPDF, rows: [string, string][], startY: number) {
  let y = startY;
  doc.setDrawColor("#E8EAFF");
  doc.setFillColor(BRAND.soft);
  doc.roundedRect(14, y, 182, rows.length * 10 + 8, 3, 3, "FD");
  y += 8;
  rows.forEach(([label, value]) => {
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    doc.setTextColor(BRAND.muted);
    doc.text(label.toUpperCase(), 20, y);
    doc.setFont("helvetica", "bold");
    doc.setFontSize(11);
    doc.setTextColor(BRAND.text);
    doc.text(value, 80, y);
    y += 10;
  });
  return y + 4;
}

function footer(doc: jsPDF, refLabel: string, ref: string) {
  doc.setDrawColor("#E8EAFF");
  doc.line(14, 268, 196, 268);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(9);
  doc.setTextColor(BRAND.muted);
  doc.text(`${refLabel}: ${ref}`, 14, 276);
  doc.text("Generated by VelaGo · Keep this confirmation for your records.", 14, 282);
}

