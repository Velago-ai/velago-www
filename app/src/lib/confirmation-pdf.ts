  // Boarding-pass strip
  doc.setFillColor("#FFFFFF");
  doc.setDrawColor("#E8EAFF");
  doc.roundedRect(14, y, 182, 38, 3, 3, "FD");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(28);
  doc.setTextColor(BRAND.primary);
  doc.text("LHR", 24, y + 24);
