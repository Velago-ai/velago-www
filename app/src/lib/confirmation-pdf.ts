  doc.setFontSize(10);
  doc.setFont("helvetica", "normal");
  doc.text("→ 2h 40m · non-stop", 70, y + 16);
  // fake barcode
  for (let i = 0; i < 60; i++) {
    if (Math.random() > 0.45) {
      doc.setFillColor(BRAND.text);
      doc.rect(20 + i * 2.7, y + 30, Math.random() > 0.5 ? 1.2 : 0.6, 5, "F");
    }
  }

  footer(doc, "Booking ref", ref);
  return doc;
