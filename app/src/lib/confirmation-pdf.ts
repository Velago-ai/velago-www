function foodOrder(session: ChatSession): jsPDF {
  const doc = new jsPDF();
  const ref = `DEL-${Math.floor(1000 + Math.random() * 9000)}`;
  header(doc, "Order confirmation", "Deliveroo · Thai Square");

  let y = detailsBlock(
    doc,
    [
      ["Customer", "Alex Johnson"],
      ["Deliver to", "12 Baker Street, London"],
      ["Restaurant", "Thai Square · Marylebone"],
      ["Estimated", "Today, 19:30 – 19:50"],
      ["Driver", "Notified once collected"],
    ],
    60,
  );

  // Items table
  doc.setFont("helvetica", "bold");
  doc.setFontSize(11);
  doc.setTextColor(BRAND.text);
  doc.text("Items", 14, y);
  y += 4;
  doc.setDrawColor("#E8EAFF");
  doc.line(14, y, 196, y);
  y += 6;

  const items: [string, string, string][] = [
    ["2 ×", "Pad Thai (chicken)", "£18.00"],
    ["1 ×", "Vegetable spring rolls", "£4.50"],
    ["1 ×", "Thai iced tea", "£2.49"],
  ];
  doc.setFont("helvetica", "normal");
  doc.setFontSize(11);
  for (const [qty, name, price] of items) {
    doc.text(qty, 14, y);
    doc.text(name, 30, y);
    doc.text(price, 180, y, { align: "right" });
    y += 7;
  }

  y += 2;
  doc.line(14, y, 196, y);
  y += 6;
  doc.setFont("helvetica", "bold");
  doc.text("Total", 14, y);
  doc.text("£24.99", 180, y, { align: "right" });

  footer(doc, "Order ID", ref);
  return doc;
}

export function downloadConfirmation(session: ChatSession) {
  const builders: Record<Category, (s: ChatSession) => jsPDF> = {
    flight: airTicket,
    parcel: parcelLabel,
    food: foodOrder,
    hotel: foodOrder, // fallback
  };
  const doc = builders[session.category](session);
  const safeTitle = session.title.replace(/[^a-z0-9]+/gi, "-").toLowerCase();
  doc.save(`velago-${safeTitle}.pdf`);
}

export function confirmationLabel(category: Category): string {
  if (category === "flight") return "Air ticket (PDF)";
  if (category === "parcel") return "Parcel label (PDF)";
  if (category === "food") return "Order confirmation (PDF)";
  return "Confirmation (PDF)";
}
